/*. O que é Node.js e qual é a sua principal finalidade no desenvolvimento web?
Node.js é um ambiente de tempo de execução JavaScript de código aberto que permite 
executar código JavaScript no servidor. Sua principal finalidade no desenvolvimento web 
é permitir a criação de aplicações de servidor altamente escaláveis e eficientes, manipulando solicitações 
e fornecendo respostas de forma assíncrona e não bloqueante*/
