/*4. Qual é a diferença entre o modelo de programação síncrona e o modelo de
programação assíncrona em Node.js? Forneça exemplos de quando cada modelo
é mais apropriado.
No modelo de programação síncrona, as operações bloqueiam a execução do código até que estejam concluídas, enquanto no modelo assíncrono, as operações não 
bloqueiam o código, permitindo que outras operações continuem enquanto aguardam resultados. O modelo síncrono é apropriado 
quando a ordem de execução é crítica, enquanto o modelo assíncrono é preferível para operações que podem ser realizadas em paralelo, como E/S 
de arquivo ou solicitações de rede.*/